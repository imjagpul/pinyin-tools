<h2>...with mnemonics</h2>
<p>
Writing your own mnemonics for every character is a method that works very well.
It helps you remember the characters and pronunciation much easier than simple rote learning.
</p>
<p>
But even the mnemonics, when not repeated, tend to disappear from our memory. 
That is why I devised a simple method of repeating them - they are linked directly to the text you are reading
(be it on your computer screen or any other device). 
</p>
<p>
Every time you look at a character translation (and it takes only a second) you will see your mnemonic as well - which 
will instantly refresh your knowledge - and you will grow familiar with all characters in&nbsp;a&nbsp;fraction of the time you would need when learning by flashcards.

</p>

