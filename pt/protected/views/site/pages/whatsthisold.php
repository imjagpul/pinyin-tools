<p>


Ok. In order to remember a single hanzi character, you need to know 
how to write it (the parts it is composed of), how to pronounce it (the pinyin and the tone) and the meaning of it.

And the trick we are using here is to join these words together in a "story", in a mental picture.
If you need to remind yourself of how to write or pronounce the character,
you just need to remember the story and you will remember it all 
(that makes it much faster and easier than rote learning).
